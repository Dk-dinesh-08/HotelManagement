package com.enumurations;

/**
 * The RoomType enum represents different types of rooms.
 * @author Dinesh Karmegam(expleo)
 * @since 18 feb 2024
 */
public enum RoomType {
    /**
     * Represents a single AC room.
     */
    SINGLE_AC,
    
    /**
     * Represents a double AC room.
     */
    DOUBLE_AC,
    
    /**
     * Represents a suite AC room.
     */
    SUITE_AC,
    
    /**
     * Represents a single non-AC room.
     */
    SINGLE_NON_AC,
    
    /**
     * Represents a double non-AC room.
     */
    DOUBLE_NON_AC,
    
    /**
     * Represents a suite non-AC room.
     */
    SUITE_NON_AC
}
