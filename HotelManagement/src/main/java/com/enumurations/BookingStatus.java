package com.enumurations;

/**
 * The BookingStatus enum represents the status of a booking.
 * @author Dinesh Karmegam(expleo)
 * @since 18 feb 2024
 */
public enum BookingStatus {
    /**
     * Represents a booked status.
     */
    BOOKED,
    
    /**
     * Represents a canceled status.
     */
    CANCELED,
    
    /**
     * Represents an available status.
     */
    AVAILABLE;
}
